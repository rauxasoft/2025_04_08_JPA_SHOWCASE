CREATE TABLE LIBROS(

	ISBN			VARCHAR(50)			NOT NULL,
	TITULO			VARCHAR(200)		,
	NUMERO_PAGINAS	INTEGER				,
	EXPLICITO		BOOLEAN				,
	
	PRIMARY KEY (ISBN)

);

CREATE TABLE JUGUETES(

	ID				BIGINT				NOT NULL, 
	NOMBRE			VARCHAR(200)		,
	PRECIO			DOUBLE				,
	TIPO			VARCHAR(20)			,
	FECHA_ALTA		DATE				,
	
	PRIMARY KEY(ID)
	
);