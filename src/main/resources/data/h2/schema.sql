CREATE TABLE PROVEEDORES(

	ID				BIGINT				NOT NULL,
	NOMBRE			VARCHAR(100)		,
	VIA				VARCHAR(100)		,
	POBLACION		VARCHAR(100)		,
	CODIGO_POSTAL	VARCHAR(10)			,
	PROVINCIA		VARCHAR(100)		,
	PAIS			VARCHAR(100)		,
	
	PRIMARY KEY(ID)

);

CREATE TABLE EQUIPOS(

	ID				BIGINT				NOT NULL,
	NOMBRE			VARCHAR(100)		,
	FECHA_FUNDACION	DATE				,
	
	PRIMARY KEY(ID)

);

CREATE TABLE JUGADORES(

	ID				BIGINT				NOT NULL,
	NOMBRE			VARCHAR(200)		,
	DORSAL			INTEGER				,
	ID_EQUIPO		BIGINT				,
	
	PRIMARY KEY(ID),
	FOREIGN KEY(ID_EQUIPO) REFERENCES EQUIPOS (ID)
);

CREATE TABLE LIBROS(

	ISBN			VARCHAR(50)			NOT NULL,
	TITULO			VARCHAR(200)		,
	NUMERO_PAGINAS	INTEGER				,
	EXPLICITO		BOOLEAN				,
	
	PRIMARY KEY (ISBN)

);

CREATE TABLE JUGUETES(

	ID				BIGINT				NOT NULL, 
	NOMBRE			VARCHAR(200)		,
	PRECIO			DOUBLE				,
	TIPO			VARCHAR(20)			,
	FECHA_ALTA		DATE				,
	
	PRIMARY KEY(ID)
	
);